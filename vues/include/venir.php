<h2>La mairie : <i>Versailles</i> la splendide</h2>
<p>
  Le mariage civil se déroulera à la mairie de Versailles, dans l'hôtel de ville.<br/>
  Son adresse est le 4 avenue de Paris, 78011 Versailles.<br/>
  Précautions à prendre : on ne peut pas se garer devant la mairie. Il y a des contre-allées alentour dans lesquelles il y a souvent  des places libres, ou un parking payant après la gare de Versailles Rive Droite, à gauche après la gare routière.
</p>

<h2>L'église : la presque cathédrale <i>Notre Dame du Chêne</i></h2>
<p>
  Le mariage aura lieu au Domaine de Brunel, près de Drocourt.<br/>
  Adresse : 28 Rue Rieussec, 78220 Viroflay.<br/>
  Pareillement, il y a quelques places autour de l'église. De plus, le coin est en travaux, suite à la création de la ligne de tram T6. Il y a quand même des rues autour dans lesquelles vous pourrez vous garer.
</p>

<h2>Réception au merveilleux <i>Domaine de Brunel</i></h2>
<p>
  La réception aura lieu au Domaine de Brunel, près de Drocourt.<br/>
  Très précisément au 22 sente Jean Brunel , 95 510 Aincourt.<br/>
  Plein de places pour tout le monde. Venez nombreux.
</p>

