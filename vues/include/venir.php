<h2>Venir en transport en commun</h2>
<h3>Pour la mairie</h3>
<p>
	Comme il s'agit de l'hôtel de ville, c'est assez facilement accessible. Vous pouvez venir via le <span class="label label-inverse">RER C, arrêt Versailles Droite</span>, ou le <span class="label label-inverse">RER C, arrêt Gare de Chantiers</span>. Via la <span class="label label-inverse">ligne L</span>, vous arriverez à <span class="label label-inverse">Versailles Rive Gauche</span>, mais il vous faudra prendre le bus D pour arriver à destination.
</p>

<h3>Pour l'église</h3>
<p>
	Deux stations permettent de venir facilement. Arrivée <span class="label label-inverse">Viroflay Rive Droite</span>, par le <span class="label label-inverse">RER C</span> ou la <span class="label label-inverse">ligne N</span>, vous	arriverezjuste à côté de 	l'église. La <span class="label label-inverse">ligne L</span> vous emmenera à <span class="label label-inverse">Viroflay Rive 	Gauche</span>, mais il faudra marcher un peu plus pour arriver à destination (rassurez-vous, ça prend moins de 5 minutes).
</p>

<h3>Pour la réception</h3>
<p>
	Pour Mantes-la-Jolie, il y a quelques trains, mais il faudra prendre un taxi pour aller de la gare jusqu'au lieu du mariage. Et	oui, un lieu romantique perdu dans les champs, c'est souvent loin des transports en commun.
</p>